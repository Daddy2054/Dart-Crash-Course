void main(List<String> args) {
  /// 4 types of operators
  /// unary prefix, unary postfix, binary infix, compound assignment
  ///
  /// unary prefix
  /// ++, --, !, ~, -, +

  int age = 30;
  print(--age);

  print(!true);

  print(++age);

  print(age);

  /// unary bitwiswe complement prefix operator
  ///
  ///

  print(~1);

  print(-age);
}
